

//toggle Address input field

function popAddressForm(){
    document.querySelector(".AddressForm").classList.toggle("AddressFormVisible");
    document.querySelector(".NewAddressBtn").classList.toggle("NewAddressBtnSlide");
}

//close Address input field
function closepopAddressForm(){
    document.querySelector(".AddressForm").classList.remove("AddressFormVisible");
    document.querySelector(".NewAddressBtn").classList.remove("NewAddressBtnSlide");
}






